package com.edwinabrenda.mapsdemo;

public class User {

    String id;
    String username;
    String email;
    String contact;
    String password;
    String cpassword;

    public User(String id, String username, String email, String contact, String password, String cpassword) {
        this.id = id;
        this.username = username;
        this.email = email;
        this.contact = contact;
        this.password = password;
        this.cpassword = cpassword;
    }

}
